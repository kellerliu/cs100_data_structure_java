
public class pictureloop {
	public static void main(String[] args) {
		pictureloop p = new pictureloop();
		p.draw();}
	
	

	public void draw(){	
			String starString = "";
			for(int i = 0; i < 10; i++)
			{
			   starString = starString + "*";
			   System.out.println(starString);
			
			}
		}
		
		

}
