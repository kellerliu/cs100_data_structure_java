public class Car {

	String model;
	String make;
	String color;
	int year;

	public Car(String a, String b, String c, int y) {

		model = a;
		make = b;
		color = c;
		year = y;

	}

	public String getModel() {
		return model;
	}

	public String getColor() {
		return color;
	}

}
